const char *i18n(StrId id) {
  int lang = config_lang();
  if (lang < 0 || lang >= LANG_COUNT) { lang = LANG_EN; }
  if ((unsigned)id >= (unsigned)STR_COUNT) { return ""; }
  const char *s = S[lang][id];
  return s ? s : (S[LANG_EN][id] ? S[LANG_EN][id] : "");
}
